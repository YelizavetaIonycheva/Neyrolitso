//Промежуточные результаты
static unsigned char IVp[64], Np[64], Sp[64];

//-----------------------------------------------------------------
//Сложение по модулю 2
//-----------------------------------------------------------------
static void AddMod2(unsigned char *sum, unsigned char *s1, unsigned char *s2)
{
	int i;

	for(i = 0; i < 64; i++)
	{
		sum[i] = s1[i] ^ s2[i];
	}
}

//-----------------------------------------------------------------
//Преобразования L, P, S
//-----------------------------------------------------------------
static void LPS(unsigned char *K)
{
	int i, ii, j;
	unsigned char TMP[64];
	unsigned int temp[2] = {0, 0};

	//Преобразование S
	for(i = 0; i < 64; i++)
	{
		K[i] = pi[K[i]];
	}
	//Преобразование P
	for(i = 0; i < 64; i++)
	{
		TMP[i] = K[t[i]];
	}
	//Преобразование L
	for(i = 0; i < 8; i++)
	{
		for(ii = 0; ii < 8; ii++)
		{
			for(j = 0; j < 8; j++)
			{
				if((TMP[i * 8 + ii] >> j) & 1)
				{
					temp[0] ^= A[2 * (63 - (ii * 8 + j))];
					temp[1] ^= A[2 * (63 - (ii * 8 + j)) + 1];
				}
			}
		}
		K[i * 8]     = *((unsigned char *)&temp[0]);
		K[i * 8 + 1] = *((unsigned char *)&temp[0] + 1);
		K[i * 8 + 2] = *((unsigned char *)&temp[0] + 2);
		K[i * 8 + 3] = *((unsigned char *)&temp[0] + 3);
		K[i * 8 + 4] = *((unsigned char *)&temp[1]);
		K[i * 8 + 5] = *((unsigned char *)&temp[1] + 1);
		K[i * 8 + 6] = *((unsigned char *)&temp[1] + 2);
		K[i * 8 + 7] = *((unsigned char *)&temp[1] + 3);
		temp[0] = 0;
		temp[1] = 0;
	}
}

//-----------------------------------------------------------------
//Преобразование E
//-----------------------------------------------------------------
static void E(unsigned char *tt, unsigned char *K, unsigned char *m)
{
	int i;

	AddMod2(tt, K, m);
	//Итерации
	for(i = 0; i < 12; i++)
	{
		LPS(tt);
		//Вычисление К
		AddMod2(K, C[i], K);
		LPS(K);
		AddMod2(tt, K, tt);
	}
}

//-----------------------------------------------------------------
//Функция сжатия
//-----------------------------------------------------------------
static void gN(unsigned char *h, unsigned char *m, unsigned char *N)
{
	unsigned char tt[64], K[64];

	AddMod2(K, h, N);
	LPS(K);
	E(tt, K, m);
	AddMod2(tt, h, tt);
	AddMod2(h, m, tt);
}

//-----------------------------------------------------------------
//Сложение (C = A + B)
//-----------------------------------------------------------------
static void Add(unsigned char *C, unsigned char *A, unsigned char *B)
{
	int i, t = 0;

	for(i = 0; i < 64; i++)
	{
		t = A[i] + B[i] + (t >> 8);
		C[i] = t & 0xFF;
	}
}

